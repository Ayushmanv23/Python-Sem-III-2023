#Arthimatical operators.
num1 = input("Enter the first number: ")
num2 = input("Enter the second number: ")

print(int(num1) + int(num2))            #addition

print(int(num1) - int(num2))            #subtraction

print(int(num1) * int(num2))            #multiply

print(float(num1) / float(num2))        #division

print(int(num1) // int(num2))           #only int value return

print(int(num1) ** int(num2))           #power function

